/**
 * Build src/data/vehicleValuationBands.json from compact tuples.
 * Tuple: [year_from, year_to, w1, w2, b1, b2] — min/max normalized in pack().
 * Run: node scripts/buildVehicleValuationBands.js
 */
const fs = require('fs');
const path = require('path');

function pack(make, model, rows) {
  return rows.map(([year_from, year_to, w1, w2, b1, b2]) => {
    const w2f = w2 == null ? w1 : w2;
    const b2f = b2 == null ? b1 : b2;
    return {
      make,
      model,
      year_from,
      year_to,
      worst_min: Math.min(w1, w2f),
      worst_max: Math.max(w1, w2f),
      best_min: Math.min(b1, b2f),
      best_max: Math.max(b1, b2f),
    };
  });
}

